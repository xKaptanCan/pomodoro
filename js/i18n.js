    getRandomQuote() {
        const quotes = this.motivationQuotes[this.currentLang] || this.motivationQuotes['en'];
        const index = Math.floor(Math.random() * quotes.length);
        return quotes[index];
    },

    getDailyQuote() {
        const quotes = this.motivationQuotes[this.currentLang] || this.motivationQuotes['en'];
        const today = new Date().toDateString();
        let hash = 0;
        for (let i = 0; i < today.length; i++) {
            hash = ((hash << 5) - hash) + today.charCodeAt(i);
            hash = hash & hash;
        }
        const index = Math.abs(hash) % quotes.length;
        return quotes[index];
    },

    init() {
        const savedLang = Storage.get('language', 'en');
        this.setLanguage(savedLang);
        this.bindDropdown();
    },

    bindDropdown() {
        const dropdown = document.getElementById('langDropdown');
        const toggle = document.getElementById('langToggle');
        const menu = document.getElementById('langMenu');

        if (!dropdown || !toggle || !menu) return;

        // Toggle dropdown
        toggle.addEventListener('click', (e) => {
            e.stopPropagation();
            dropdown.classList.toggle('open');
        });

        // Language options
        menu.querySelectorAll('.lang-option').forEach(option => {
            option.addEventListener('click', (e) => {
                e.stopPropagation();
                const lang = option.dataset.lang;
                this.setLanguage(lang);
                dropdown.classList.remove('open');
            });
        });

        // Close on outside click
        document.addEventListener('click', () => {
            dropdown.classList.remove('open');
        });

        // Update active state
        this.updateDropdownActive();
    },

    updateDropdownActive() {
        const menu = document.getElementById('langMenu');
        if (!menu) return;

        menu.querySelectorAll('.lang-option').forEach(option => {
            option.classList.remove('active');
            if (option.dataset.lang === this.currentLang) {
                option.classList.add('active');
            }
        });
    },

    setLanguage(lang) {
        if (!this.translations[lang]) lang = 'en';
        this.currentLang = lang;
        Storage.set('language', lang);
        this.updateUI();

        // Update display
        const currentLangEl = document.getElementById('currentLang');
        if (currentLangEl) {
            currentLangEl.textContent = lang.toUpperCase();
        }

        this.updateDropdownActive();
    },

    toggleLanguage() {
        // For backwards compatibility - cycles through languages
        const langs = Object.keys(this.translations);
        const currentIndex = langs.indexOf(this.currentLang);
        const nextIndex = (currentIndex + 1) % langs.length;
        this.setLanguage(langs[nextIndex]);
    },

    t(key) {
        return this.translations[this.currentLang]?.[key] || this.translations['en']?.[key] || key;
    },

    updateUI() {
        // Update all elements with data-i18n attribute
        document.querySelectorAll('[data-i18n]').forEach(el => {
            const key = el.getAttribute('data-i18n');
            el.textContent = this.t(key);
        });

        // Update placeholders
        document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
            const key = el.getAttribute('data-i18n-placeholder');
            el.placeholder = this.t(key);
        });

        // Update page title based on timer state
        if (typeof App !== 'undefined' && App.timer) {
            App.updateTitle();
            App.updateModeUI(App.currentMode);
            App.updateDailyQuote();
        }

        // Re-render tasks to update noTasks message
        if (typeof Tasks !== 'undefined') {
            Tasks.render();
        }
    }
